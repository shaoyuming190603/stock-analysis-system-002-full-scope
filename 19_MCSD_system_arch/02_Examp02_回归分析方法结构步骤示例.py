import pandas as pd

# 假设数据已准备好
data = pd.read_csv('maotai_factors.csv', index_col=0, parse_dates=True)
# columns: ['return', 'PE', 'turnover', 'industry_return']


#### 输出数据
#- 回归分析所需的因变量和自变量数据表


### 步骤2：回归建模与计算

#### 计算公式
# - 线性回归模型：
#   \( return_t = \beta_0 + \beta_1 PE_t + \beta_2 turnover_t + \beta_3 industry\_return_t + \epsilon_t \)

#### 代码示例

import statsmodels.api as sm

X = data
Y = data['return']
X = sm.add_constant(X)  # 加常数项
model = sm.OLS(Y, X).fit()
print(model.summary())
# ```

#### 输出数据
# - 回归系数、t值、p值、R²等统计量


### 步骤3：结果解读与行动指导

#### 关键输出指标
# - **回归系数**：因子对收益的边际影响
# - **p值**：因子显著性（p&lt;0.05为显著）
# - **R²**：模型解释能力
# - **残差分析**：模型拟合优劣

#### 行动指导
# - 若某因子系数显著且为正（如行业指数收益），可在行业景气时加大配置。
# - 若PE系数为负，说明估值高时收益低，可作为择时参考。
# - 若模型R²较高，说明因子解释力强，可用于预测和策略制定。


### 步骤4：预测、反馈与优化

#### 预测建模
# - 用模型预测未来收益（输入未来PE、换手率、行业收益预测值）。
# - 预测结果作为买卖决策参考。

#### 验证与反馈
# - **回测**：用历史数据检验预测准确率。
# - **优劣评判**：如均方误差（MSE）、信息比率、预测胜率等。
# - **迭代优化**：如因子筛选、模型调整、引入非线性项等。

#### 代码示例

# 预测未来收益
future_X = sm.add_constant(pd.DataFrame({'PE':[30], 'turnover':[0.02], 'industry_return':[0.01]}))
predicted_return = model.predict(future_X)
print("预测收益率：", predicted_return[0])


## 三、输入-输出-反馈控制体系

# | 阶段         | 输入数据               | 输出数据           | 反馈/控制           |
# |--------------|-----------------------|--------------------|---------------------|
# | 数据收集     | 收益率、因子数据       | 回归输入表         | 数据完整性检查      |
# | 回归建模     | 回归输入表             | 回归系数、R²、p值  | 模型显著性检验      |
# | 结果解读     | 回归输出               | 因子有效性、策略建议| 策略调整            |
# | 预测优化     | 新因子、新数据         | 预测结果、回测指标 | 迭代优化            |


## 四、数值化优劣评判与迭代优化

# - **R²**：越高越优，说明模型解释力强。
# - **p值**：小于0.05为有效因子。
# - **均方误差（MSE）**：越低越优。
# - **信息比率、预测胜率**：用于策略优劣评判。
# - **回测**：检验模型在历史数据上的表现，防止过拟合。


## 五、应用场景与触发条件

# - **因子分析**：用于选股、择时、行业轮动等策略开发。
# - **预测建模**：预测未来收益、波动、风险，辅助买卖决策。
# - **触发条件**：如预测收益大于阈值则买入，低于阈值则卖出或观望。


