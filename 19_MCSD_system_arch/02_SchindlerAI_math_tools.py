# 	##	一、股票分析常用的数学与统计方法清单
#
# 	###	1	经典统计方法
# 	-	**均值、方差、标准差**
# 		-	**应用**：衡量股票收益的期望和波动性。
# 		-	**优势**：简单直观	基础性强。
# 		-	**注意事项**：假设收益分布为正态分布	实际金融市场常有“肥尾”现象。
#
# 	-	**协方差与相关系数（Pearson/Spearman）**
# 		-	**应用**：分析个股与板块、市场指数的联动性。
# 		-	**优势**：衡量线性相关性	便于资产配置。
# 		-	**注意事项**：只反映线性关系	忽略非线性相关。
#
# 	-	**回归分析（线性/多元/逻辑回归）**
# 		-	**应用**：预测股票价格、因子暴露、板块轮动。
# 		-	**优势**：可量化变量间关系	易于解释。
# 		-	**注意事项**：需满足独立性、同方差等假设	过拟合风险。
#
# 	###	2	时间序列分析
# 	-	**自回归模型（AR）、移动平均（MA）、ARMA、ARIMA、SARIMA**
# 		-	**应用**：预测股价、波动率、周期性变化。
# 		-	**优势**：适合处理金融时间序列数据	能捕捉趋势和季节性。
# 		-	**注意事项**：需数据平稳性	参数选择影响大。
#
# 	-	**GARCH/EGARCH/TGARCH**
# 		-	**应用**：建模波动率聚集效应	风险管理。
# 		-	**优势**：能刻画金融市场的波动性特征。
# 		-	**注意事项**：模型复杂	参数估计需大量数据。
#
# 	###	3	频域分析
# 	-	**傅里叶变换（FFT）/小波变换**
# 		-	**应用**：分析价格周期、市场周期性成分。
# 		-	**优势**：能揭示隐藏的周期性	适合高频数据。
# 		-	**注意事项**：对噪声敏感	需数据预处理。
#
# 	###	4	概率与贝叶斯方法
# 	-	**贝叶斯推断/贝叶斯网络**
# 		-	**应用**：概率预测、风险评估、因果关系建模。
# 		-	**优势**：能结合先验知识	适应动态更新。
# 		-	**注意事项**：先验选择影响大	计算复杂度高。
#
# 	###	5	机器学习与数据挖掘
# 	-	**主成分分析（PCA）/因子分析**
# 		-	**应用**：降维、因子暴露、板块轮动分析。
# 		-	**优势**：提取主要驱动因子	减少噪声。
# 		-	**注意事项**：解释性有限	需标准化数据。
#
# 	-	**聚类分析（K-means、层次聚类）**
# 		-	**应用**：板块划分、市场结构识别。
# 		-	**优势**：发现隐藏结构	辅助资产配置。
# 		-	**注意事项**：聚类数目需预设	结果受尺度影响。
#
# 	-	**神经网络、随机森林、支持向量机等**
# 		-	**应用**：非线性预测、模式识别。
# 		-	**优势**：适应复杂关系	预测能力强。
# 		-	**注意事项**：黑箱特性	需大量数据训练。
#
# 	###	6	其他数学工具
# 	-	**Granger因果检验**
# 		-	**应用**：判断板块间、个股与板块间的因果关系。
# 		-	**优势**：揭示时间序列间的先后影响。
# 		-	**注意事项**：需平稳性	滞后阶数选择敏感。
#
# 	-	**协整检验（Johansen、Engle-Granger）**
# 		-	**应用**：寻找长期均衡关系（如配对交易）。
# 		-	**优势**：适合套利策略	捕捉价格背离。
# 		-	**注意事项**：需大量历史数据	假设严格。
#
