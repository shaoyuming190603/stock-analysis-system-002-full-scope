# 	-	**相关系数、协方差、Granger因果检验**：衡量同步性与因果性。
# 	-	**PCA/因子分析**：提取共性因子	分析板块驱动。
#
# 	###	4	板块轮动、背离与同步
# 	-	**回归分析、协整检验**：捕捉长期关系与短期背离。
# 	-	**聚类分析、主成分分析**：识别板块结构与轮动特征。
