# 	##	二、主流分析工具在特定场景的应用
#
# 	###	1	股票价格周期分析
# 	-	**傅里叶变换、小波变换**：揭示周期性成分	适合高频数据。
# 	-	**ARIMA/SARIMA**：建模季节性和趋势性。
#
# 	###	2	波动幅度分析
# 	-	**标准差、方差、GARCH类模型**：量化波动性	风险管理常用。
#
