# 	下面以**均值-方差分析（Mean-Variance	Analysis）**为例	详细说明如何用它对5只不同特性的股票进行资产配置和风险评估	并形成投资决策。整个过程分为六个阶段	每个阶段都包含具体任务、评价指标和方法。
#
# 	---
#
# 	##	阶段一：数据收集与预处理
#
# 	###	任务重点
# 	-	收集5只股票的历史价格数据（如日收盘价	假设为A、B、C、D、E）。
# 	-	计算每只股票的日收益率（或周/月收益率）。
#
# 	###	评价指标与方法
# 	-	**收益率计算**：\(	r_{i	t}	=	\frac{P_{i	t}	-	P_{i	t-1}}{P_{i	t-1}}	\)
# 	-	**数据完整性**：无缺失、无异常值。
#
# 	---
#
# 	##	阶段二：统计特征计算
#
# 	###	任务重点
# 	-	计算每只股票的**平均收益率**和**标准差**（波动率）。
# 	-	计算5只股票之间的**协方差矩阵**。
#
# 	###	评价指标与方法
# 	-	**平均收益率**：\(	\mu_i	=	\frac{1}{N}	\sum_{t=1}^N	r_{i	t}	\)
# 	-	**标准差（波动率）**：\(	\sigma_i	=	\sqrt{\frac{1}{N-1}	\sum_{t=1}^N	(r_{i	t}	-	\mu_i)^2}	\)
# 	-	**协方差矩阵**：\(	Cov_{i	j}	=	\frac{1}{N-1}	\sum_{t=1}^N	(r_{i	t}	-	\mu_i)(r_{j	t}	-	\mu_j)	\)
#
# 	---
#
# 	##	阶段三：构建投资组合
#
# 	###	任务重点
# 	-	设定每只股票的投资权重	\(	w_i	\)（权重之和为1）。
# 	-	计算投资组合的**预期收益率**和**组合风险（标准差）**。
#
# 	###	评价指标与方法
# 	-	**组合预期收益率**：\(	\mu_p	=	\sum_{i=1}^5	w_i	\mu_i	\)
# 	-	**组合风险（方差）**：\(	\sigma_p^2	=	\sum_{i=1}^5	\sum_{j=1}^5	w_i	w_j	Cov_{i	j}	\)
# 	-	**组合标准差**：\(	\sigma_p	=	\sqrt{\sigma_p^2}	\)
#
# 	---
#
# 	##	阶段四：优化资产配置
#
# 	###	任务重点
# 	-	通过优化算法（如二次规划）寻找在给定风险下收益最大	或在给定收益下风险最小的权重组合。
# 	-	画出**有效前沿（Efficient	Frontier）**。
#
# 	###	评价指标与方法
# 	-	**夏普比率**：\(	Sharpe	=	\frac{\mu_p	-	r_f}{\sigma_p}	\)（\(	r_f	\)为无风险利率）
# 	-	**有效前沿**：所有不可被其他组合在风险-收益空间中“支配”的组合集合。
#
# 	---
#
# 	##	阶段五：风险评估与决策
#
# 	###	任务重点
# 	-	评估最优组合的风险水平	是否符合投资者的风险承受能力。
# 	-	进行情景分析和压力测试。
#
# 	###	评价指标与方法
# 	-	**VaR（在险价值）**：衡量极端情况下的最大损失。
# 	-	**最大回撤**：历史最大损失幅度。
# 	-	**分散化效应**：组合风险是否低于单一资产风险。
#
# 	---
#
# 	##	阶段六：投资执行与后续跟踪
#
# 	###	任务重点
# 	-	按照最优权重分配资金	实际买入股票。
# 	-	定期复盘	调整权重应对市场变化。
#
# 	###	评价指标与方法
# 	-	**实际收益与风险**：与预期对比。
# 	-	**组合再平衡**：定期调整权重	保持最优配置。
#
# 	---
#
# 	##	实例演示（简化版）
#
# 	假设5只股票的年化平均收益率和标准差如下：
#
# 	|	股票	|	平均收益率（μ）	|	标准差（σ）	|
# 	|------|----------------|------------|
# 	|	A	|	8%	|	15%	|
# 	|	B	|	10%	|	20%	|
# 	|	C	|	6%	|	10%	|
# 	|	D	|	12%	|	25%	|
# 	|	E	|	7%	|	12%	|
#
# 	协方差矩阵（单位：%²	简化）：
#
# 	|	|	A	|	B	|	C	|	D	|	E	|
# 	|------|-----|-----|-----|-----|-----|
# 	|	A	|225	|	60	|	30	|	50	|	40	|
# 	|	B	|	60	|400	|	35	|	70	|	55	|
# 	|	C	|	30	|	35	|100	|	25	|	20	|
# 	|	D	|	50	|	70	|	25	|625	|	60	|
# 	|	E	|	40	|	55	|	20	|	60	|144	|
#
# 	假设通过优化	得到最优权重为：
#
# 	|	股票	|	权重（w）	|
# 	|------|----------|
# 	|	A	|	20%	|
# 	|	B	|	25%	|
# 	|	C	|	15%	|
# 	|	D	|	30%	|
# 	|	E	|	10%	|
#
# 	则：
# 	-	组合预期收益率：
# 		\(	\mu_p	=	0.2*8\%	+	0.25*10\%	+	0.15*6\%	+	0.3*12\%	+	0.1*7\%	=	9.45\%	\)
# 	-	组合风险（标准差）：
# 		\(	\sigma_p	=	\sqrt{w^T	\cdot	Cov	\cdot	w}	\)（实际计算需矩阵运算	假设结果为16.5%）
#
# 	夏普比率（假设无风险利率为2%）：
# 	\(	Sharpe	=	\frac{9.45\%	-	2\%}{16.5\%}	=	0.45	\)
#
# 	---
#
# 	##	总结
#
# 	-	**均值-方差分析**通过量化收益与风险	帮助投资者在风险可控的前提下追求最大化收益。
# 	-	其核心是利用历史数据计算收益、波动、相关性	优化组合权重	形成“有效前沿”。
# 	-	风险评估不仅看标准差	还需关注VaR、最大回撤等指标。
# 	-	实际投资需定期复盘和再平衡	确保组合持续符合目标。
